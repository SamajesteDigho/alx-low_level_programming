/**
 * add - Function
 * @a: Param 1
 * @b: Param 2
 * Description: Permits to operate the of 2 numbers
 * Return: Result of the operation
*/
int add(int a, int b)
{
return (a + b);
}
/**
 * sub - Function
 * @a: Param 1
 * @b: Param 2
 * Description: Permits to operate the of 2 numbers
 * Return: Result of the operation
*/
int sub(int a, int b)
{
return (a - b);
}
/**
 * mul - Function
 * @a: Param 1
 * @b: Param 2
 * Description: Permits to operate the of 2 numbers
 * Return: Result of the operation
*/
int mul(int a, int b)
{
return (a * b);
}
/**
 * div - Function
 * @a: Param 1
 * @b: Param 2
 * Description: Permits to operate the of 2 numbers
 * Return: Result of the operation
*/
int div(int a, int b)
{
if (b == 0)
return (0);
return (a / b);
}
/**
 * mod - Function
 * @a: Param 1
 * @b: Param 2
 * Description: Permits to operate the of 2 numbers
 * Return: Result of the operation
*/
int mod(int a, int b)
{
return (a % b);
}
